    logic [5:0]  nshift;
    logic [48:0] norm;
    assign nshift = clz49(summ);
    assign norm   = summ << nshift;

    // biased exponent of the normalised result:  exp_big - nshift + 1
    logic signed [11:0] ebias_norm;
    assign ebias_norm = $signed({3'b0, exp_big}) - $signed({6'b0, nshift}) + 12'sd1;

    logic        result_sub;
    logic [11:0] rshift;
    assign result_sub = (ebias_norm <= 12'sd0);
    assign rshift = result_sub ? (12'sd1 - ebias_norm) : 12'sd0;

    // ---- normalise + round (mirrors hapi_bf16_add, base shift = 1) --------- //
    logic [31:0] arith_bits;
    always_comb begin
        logic [23:0] keep;
        logic        guard, round_b, sticky, lsb, round_up;
        logic [24:0] rounded;
        logic [9:0]  exp_final;
        logic [95:0] ext, shifted, lost_mask;
        logic [11:0] totalshift;

        keep = 24'b0; guard = 1'b0; round_b = 1'b0; sticky = 1'b0;
        lsb = 1'b0; round_up = 1'b0; rounded = 25'b0; exp_final = 10'b0;
        ext = 96'b0; shifted = 96'b0; lost_mask = 96'b0;
        totalshift = 12'd1 + rshift;

        if (summ == 49'b0) begin
            arith_bits = 32'h0000_0000;            // exact cancellation -> +0
        end else begin
            ext     = {47'b0, norm};               // implicit 1 at bit48
            shifted = ext >> totalshift[6:0];
            keep    = shifted[47:24];
            guard   = shifted[23];
            round_b = shifted[22];
            lost_mask = (96'b1 << totalshift[6:0]) - 96'b1;
            sticky    = (|shifted[21:0]) | (|(ext & lost_mask)) | align_sticky;
            lsb       = keep[0];
            round_up  = guard & (round_b | sticky | lsb);
            rounded   = {1'b0, keep} + {24'b0, round_up};

            if (result_sub) begin
                if (rounded[23] == 1'b1)
                    arith_bits = {sbig, 8'd1, rounded[22:0]};   // -> min normal
                else
                    arith_bits = {sbig, 8'd0, rounded[22:0]};   // stays subnormal
            end else begin
                exp_final = ebias_norm[9:0];
                if (rounded[24] == 1'b1)
                    exp_final = exp_final + 10'd1;
                if (exp_final >= 10'd255)
                    arith_bits = {sbig, INF[30:0]};             // overflow -> Inf
                else
                    arith_bits = {sbig, exp_final[7:0], rounded[22:0]};
            end
        end
    end

    // ---- top-level special-case mux --------------------------------------- //
    always_comb begin
        if (a_nan || b_nan || (a_inf && b_inf && (sa != sb)))
            y = QNAN;
        else if (a_inf)
            y = {sa, INF[30:0]};
        else if (b_inf)
            y = {sb, INF[30:0]};
        else if (a_zero && b_zero)
            y = {(sa & sb), 31'b0};
        else
            y = arith_bits;
    end
endmodule
