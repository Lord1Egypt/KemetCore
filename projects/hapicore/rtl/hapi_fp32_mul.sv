    logic [5:0]  lz;
    logic [47:0] norm;          // product left-normalised so bit47 = leading 1
    assign lz   = clz48(prod);
    assign norm = prod << lz;

    // ideal (pre-clamp) biased exponent:  expa + expb - 126 - lz
    logic signed [11:0] ebias_norm;
    assign ebias_norm = $signed({3'b0, expa}) + $signed({3'b0, expb})
                      - 12'sd126 - $signed({6'b0, lz});

    logic        result_sub;
    logic [11:0] rshift;
    assign result_sub = (ebias_norm <= 12'sd0);
    assign rshift = result_sub ? (12'sd1 - ebias_norm) : 12'sd0;

    logic [31:0] result_bits;
    always_comb begin
        logic [23:0] keep;
        logic        guard, round_b, sticky, lsb, round_up;
        logic [24:0] rounded;
        logic [9:0]  exp_final;
        logic [95:0] ext, shifted, lost_mask;
        logic [6:0]  effshift;

        keep = 24'b0; guard = 1'b0; round_b = 1'b0; sticky = 1'b0;
        lsb = 1'b0; round_up = 1'b0; rounded = 25'b0; exp_final = 10'b0;
        ext = 96'b0; shifted = 96'b0; lost_mask = 96'b0;
        // cap the denormal shift; beyond the field everything is captured as sticky
        effshift = (rshift > 12'd95) ? 7'd95 : rshift[6:0];

        ext     = {48'b0, norm};                  // implicit 1 at bit47
        shifted = ext >> effshift;
        keep    = shifted[47:24];
        guard   = shifted[23];
        round_b = shifted[22];
        lost_mask = (96'b1 << effshift) - 96'b1;
        sticky    = (|shifted[21:0]) | (|(ext & lost_mask));
        lsb       = keep[0];
        round_up  = guard & (round_b | sticky | lsb);
        rounded   = {1'b0, keep} + {24'b0, round_up};

        if (result_sub) begin
            if (rounded[23] == 1'b1)
                result_bits = {sy, 8'd1, rounded[22:0]};      // -> min normal
            else
                result_bits = {sy, 8'd0, rounded[22:0]};      // stays subnormal
        end else begin
            exp_final = ebias_norm[9:0];
            if (rounded[24] == 1'b1)
                exp_final = exp_final + 10'd1;
            if (exp_final >= 10'd255)
                result_bits = {sy, INF[30:0]};                // overflow -> Inf
            else
                result_bits = {sy, exp_final[7:0], rounded[22:0]};
        end
    end

    // ---- top-level special-case mux --------------------------------------- //
    always_comb begin
        if (a_nan || b_nan || (a_inf && b_zero) || (b_inf && a_zero))
            y = QNAN;
        else if (a_inf || b_inf)
            y = {sy, INF[30:0]};
        else if (a_zero || b_zero)
            y = {sy, 31'b0};
        else
            y = result_bits;
    end
endmodule
