// HapiCore — fp32 multiplier (KemetCore Phase 2 RTL)
//
// Single-cycle combinational IEEE-754 binary32 multiply. Same algorithm as the
// verified bf16 multiplier (hapi_bf16_mul), widened to a 24-bit significand:
//   * exact 24x24 significand product, leading-zero normalise (clz over 48 bits),
//     round-to-nearest-ties-to-even
//   * NaN propagation, Inf*0 -> NaN, x*0 -> 0, sign = sign_a ^ sign_b
//   * subnormal INPUTS and OUTPUTS, overflow -> Inf, underflow -> subnormal/zero
//
// fp32 layout: [31] sign | [30:23] exponent (bias 127) | [22:0] mantissa.
// Bit-exact against the Python golden / numpy fp32 — see tb/test_fp32_mul.py.
// Yosys-portable: no int'() casts, exact-width operands, casez clz.

module hapi_fp32_mul (
    input  logic [31:0] a,
    input  logic [31:0] b,
    output logic [31:0] y
);
    localparam logic [31:0] QNAN = 32'h7FC00000;
    localparam logic [31:0] INF  = 32'h7F800000;

    logic        sa, sb, sy;
    logic [7:0]  ea, eb;
    logic [22:0] ma, mb;
    assign sa = a[31];  assign ea = a[30:23];  assign ma = a[22:0];
    assign sb = b[31];  assign eb = b[30:23];  assign mb = b[22:0];
    assign sy = sa ^ sb;

    logic a_zero, b_zero, a_inf, b_inf, a_nan, b_nan, a_sub, b_sub;
    assign a_zero = (ea == 8'h00) && (ma == 23'h0);
    assign b_zero = (eb == 8'h00) && (mb == 23'h0);
    assign a_sub  = (ea == 8'h00) && (ma != 23'h0);
    assign b_sub  = (eb == 8'h00) && (mb != 23'h0);
    assign a_inf  = (ea == 8'hFF) && (ma == 23'h0);
    assign b_inf  = (eb == 8'hFF) && (mb == 23'h0);
    assign a_nan  = (ea == 8'hFF) && (ma != 23'h0);
    assign b_nan  = (eb == 8'hFF) && (mb != 23'h0);

    // 24-bit significand {implicit, mantissa}; subnormals use implicit 0, exp 1.
    logic [23:0] siga, sigb;
    logic [8:0]  expa, expb;
    assign siga = a_sub ? {1'b0, ma} : {1'b1, ma};
    assign sigb = b_sub ? {1'b0, mb} : {1'b1, mb};
    assign expa = a_sub ? 9'd1 : {1'b0, ea};
    assign expb = b_sub ? 9'd1 : {1'b0, eb};

    // exact 48-bit product of the two 24-bit significands
    logic [47:0] prod;
    assign prod = siga * sigb;

