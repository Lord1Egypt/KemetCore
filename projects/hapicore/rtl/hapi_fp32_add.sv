// HapiCore — fp32 adder (KemetCore Phase 2 RTL)
//
// Single-cycle combinational IEEE-754 binary32 add. Same algorithm as the verified
// bf16 adder (hapi_bf16_add), widened to a 24-bit significand:
//   * larger-magnitude operand chosen by an unsigned compare of {exp,mantissa}
//   * small operand aligned with guard/round/sticky, effective add or subtract
//   * leading-zero normalise (clz over a 49-bit field) + round-to-nearest-even
//   * cancellation -> +0, signed-zero rules, Inf+Inf(opp) -> NaN, subnormal in/out,
//     overflow -> Inf, underflow -> subnormal/zero
//
// fp32 layout: [31] sign | [30:23] exponent (bias 127) | [22:0] mantissa.
// This unblocks the BastCore tensor-core fp32 accumulate. Bit-exact against the
// Python golden / numpy fp32 — see tb/test_fp32_add.py.
// Yosys-portable: no int'() casts, exact-width operands, casez clz.

module hapi_fp32_add (
    input  logic [31:0] a,
    input  logic [31:0] b,
    output logic [31:0] y
);
    localparam logic [31:0] QNAN = 32'h7FC00000;
    localparam logic [31:0] INF  = 32'h7F800000;

    // ---- field extraction ------------------------------------------------- //
    logic        sa, sb;
    logic [7:0]  ea, eb;
    logic [22:0] ma, mb;
    assign sa = a[31];  assign ea = a[30:23];  assign ma = a[22:0];
    assign sb = b[31];  assign eb = b[30:23];  assign mb = b[22:0];

    logic a_zero, b_zero, a_inf, b_inf, a_nan, b_nan;
    assign a_zero = (ea == 8'h00) && (ma == 23'h0);
    assign b_zero = (eb == 8'h00) && (mb == 23'h0);
    assign a_inf  = (ea == 8'hFF) && (ma == 23'h0);
    assign b_inf  = (eb == 8'hFF) && (mb == 23'h0);
    assign a_nan  = (ea == 8'hFF) && (ma != 23'h0);
    assign b_nan  = (eb == 8'hFF) && (mb != 23'h0);

    // ---- pick larger-magnitude operand (big) vs smaller (small) ----------- //
    logic a_is_big;
    assign a_is_big = (a[30:0] >= b[30:0]);

    logic        sbig, ssml;
    logic [7:0]  ebig, esml;
    logic [22:0] mbig, msml;
    always_comb begin
        if (a_is_big) begin
            sbig = sa; ebig = ea; mbig = ma;
            ssml = sb; esml = eb; msml = mb;
        end else begin
            sbig = sb; ebig = eb; mbig = mb;
            ssml = sa; esml = ea; msml = ma;
        end
    end

    logic       big_e0, sml_e0;
    assign big_e0 = (ebig == 8'h00);
    assign sml_e0 = (esml == 8'h00);
    logic [23:0] sig_big, sig_sml;
    logic [8:0]  exp_big, exp_sml;
    assign sig_big = big_e0 ? {1'b0, mbig} : {1'b1, mbig};
    assign sig_sml = sml_e0 ? {1'b0, msml} : {1'b1, msml};
    assign exp_big = big_e0 ? 9'd1 : {1'b0, ebig};
    assign exp_sml = sml_e0 ? 9'd1 : {1'b0, esml};

    logic        eff_sub;
    assign eff_sub = sa ^ sb;
    logic [8:0]  exp_diff;
    assign exp_diff = exp_big - exp_sml;

    // ---- align the small operand into a 49-bit field ---------------------- //
    // sig at bits [47:24]; 24 guard bits below for exact alignment, bit48 = carry.
    logic [48:0] big_f, sml_full, sml_aligned;
    logic        align_sticky;
    assign big_f    = {1'b0, sig_big, 24'b0};
    assign sml_full = {1'b0, sig_sml, 24'b0};

    always_comb begin
        logic [5:0]  sh;
        logic [48:0] lost_mask;
        sh        = exp_diff[5:0];
        lost_mask = (49'b1 << sh) - 49'b1;
        if (exp_diff >= 9'd49) begin
            sml_aligned  = 49'b0;
            align_sticky = (sig_sml != 24'b0);
        end else begin
            sml_aligned  = sml_full >> sh;
            align_sticky = (|(sml_full & lost_mask));
        end
    end

    logic [48:0] summ;
    assign summ = eff_sub ? (big_f - sml_aligned) : (big_f + sml_aligned);

